<?php
namespace Admin\Controller;
use Think\Controller;
class TagController extends Controller {
    public function edittag(){
        $this -> display();
}
// 添加标签
public function addtag(){
if(!IS_POST){
$this -> error("错误的请求");
}
else{
//实例化标签模型对象
$tagsModel = M('tags');
$data = I('tagname');
if($tagsModel->add($data)){
$this -> success("添加成功");
}
else{
$this -> error("failed");
}



}

}
public function tag(){
//实例化标签模型对象
$tagsModel = M('tags');
$list = $tagsModel -> order('tagid desc') -> select();
$this -> assign("list",$list);
$this -> display();
}
}